export function enumToLabel(dic, enumeration) {
  const name = Object.keys(enumeration[0])[0]
  return dic.find((item) => item.name === name).label
}

export function nameToLabel(dic, name) {
  if (!name) return ''
  return dic.find((item) => item.name === name).label
}
